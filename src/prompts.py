COUNTRY_INFO = {
    "vietnam": {
        "country_ko": "베트남",
        "language_ko": "베트남어",
        "language_code": "vi",
    },
    "cambodia": {
        "country_ko": "캄보디아",
        "language_ko": "크메르어",
        "language_code": "km",
    },
    "myanmar": {
        "country_ko": "미얀마",
        "language_ko": "미얀마어",
        "language_code": "my",
    },
}


SYSTEM_PROMPT = """
당신은 동남아 현지 마케팅 담당자를 위한 광고/마케팅 기획서 번역 Agent입니다.

당신의 목표는 단순 번역이 아닙니다.
한국어 광고/마케팅 기획서에 포함된 마케팅 용어, 밈, 은어, 금융 표현, 위험 표현을 해석하고,
대상 국가의 현지 담당자가 이해할 수 있도록 자연스럽고 전문적인 문서형 번역을 제공해야 합니다.

중요 규칙:
1. 광고 카피처럼 과하게 재창작하지 말고, 기획서 문체를 유지하세요.
2. 밈/은어는 직역하지 말고 의미 중심으로 번역하세요.
3. 필요하면 번역 결과 아래에 한국어로 현지화 메모를 제공하세요.
4. 금융/광고 위험 표현은 반드시 검토하세요.
5. 대상 언어 번역 결과는 해당 언어로 작성하세요.
6. 설명, 용어 해석, 현지화 메모, 검토 결과는 한국어로 작성하세요.
7. 원문의 전략적 의미가 사라지지 않도록 하세요.
"""


def build_user_prompt(
    text: str,
    target_country: str,
    output_style: str,
    context: dict[str, str],
) -> str:
    country = COUNTRY_INFO[target_country]

    return f"""
다음 한국어 광고/마케팅 기획서를 {country["language_ko"]}로 번역하세요.

[대상 국가]
{country["country_ko"]}

[대상 언어]
{country["language_ko"]}

[요청 문체]
{output_style}

[원문]
{text}

[검색된 참고 정보]
{context["marketing_terms"]}

{context["slang_terms"]}

{context["finance_terms"]}

{context["risk_expressions"]}

[작업 지시]
아래 형식을 반드시 지켜서 답변하세요.

## 1. 문서 분석
- 이 문서가 어떤 목적의 광고/마케팅 기획서인지 요약하세요.
- 핵심 메시지와 대상 고객을 추정하세요.

## 2. {country["language_ko"]} 번역 결과
- 원문의 구조를 최대한 유지하면서 번역하세요.
- 기획서 문체를 유지하세요.
- 밈/은어는 직역하지 말고 의미 중심으로 번역하세요.
- 문단이 여러 개라면 문단 구조를 유지하세요.

## 3. 감지된 마케팅 용어
- 원문에서 발견된 마케팅 용어를 정리하세요.
- 각 용어의 의미를 쉽게 설명하세요.

## 4. 감지된 밈/은어/트렌드 표현
- 직역하면 어색한 표현을 정리하세요.
- 왜 직역하면 안 되는지 설명하세요.
- 기획서에서는 어떤 의미로 이해해야 하는지 설명하세요.

## 5. 현지화 메모
- {country["country_ko"]} 현지 담당자가 이해할 때 주의해야 할 점을 정리하세요.
- 현지 언어에서 과도하게 캐주얼하거나 부자연스러울 수 있는 표현은 설명 중심으로 바꾸세요.

## 6. 위험 표현 검토
- 과장 표현, 보장 표현, 오해 소지가 있는 표현이 있는지 검토하세요.
- 문제가 있다면 안전한 대체 표현을 제안하세요.
- 문제가 없다면 "큰 위험 표현은 발견되지 않았습니다."라고 작성하세요.

## 7. 최종 검토
- 번역 톤이 기획서에 적합한지 평가하세요.
- 용어 일관성 문제가 있는지 확인하세요.
- 현지인 검수가 필요한 부분이 있다면 표시하세요.
"""